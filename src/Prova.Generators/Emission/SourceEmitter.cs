                        for (int i = 0; i < method.ParameterTypes.Count; i++)
                        {
                            string paramType = method.ParameterTypes[i];
                            for (int j = 0; j < method.ClassTypeParams.Count; j++)
                                paramType = paramType.Replace(method.ClassTypeParams[j], cv[j]);
                            for (int j = 0; j < method.MethodTypeParams.Count; j++)
                                paramType = paramType.Replace(method.MethodTypeParams[j], mv[j]);

                            string castArg = $"({paramType})dataRow[{i}]";
                            castArgs.Add(castArg);

                            string? fmt = method.ParameterFormatters.Count > i ? method.ParameterFormatters[i] : null;
                            if (fmt != null)
                                formattingArgsList.Add($"new {fmt}().Format({castArg})");
                            else
                                formattingArgsList.Add(castArg);
                        }
                        int probeId = probeCounter++;
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(ClassData)");
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(ClassData)");
                        GenerateTestRegistration(sb, method, concreteClassName, concreteMethodName, string.Join(", ", castArgs), string.Join(", ", formattingArgsList), $"{variantSuffix}{classSuffix}({{string.Join(\", \", dataRow)}})", classFixtures, probeId, classRowStr, isClassDataRow, variantName, cv, mv);
                        sb.AppendLine("                }");
                        sb.AppendLine("            }");
                    }
                }

                if (method.ClassDataSources.Count > 0)
                {
                    foreach (var cdType in method.ClassDataSources)
                    {
                        sb.AppendLine($"            // ClassDataSource: {cdType}");
                        sb.AppendLine($"            foreach (var dataRow in (global::System.Collections.Generic.IEnumerable<object[]>)TestRunnerExecutor.Services.Get<{cdType}>())");
                        sb.AppendLine("            {");
                        sb.AppendLine("                if (dataRow != null)");
                        sb.AppendLine("                {");
                        
                        var castArgs = new List<string>();
                        var formattingArgsList = new List<string>();

                        for (int i = 0; i < method.ParameterTypes.Count; i++)
                        {
