                            for (int j = 0; j < method.MethodTypeParams.Count; j++)
                                paramType = paramType.Replace(method.MethodTypeParams[j], mv[j]);

                            string castArg = $"({paramType})dataRow[{i}]";
                            castArgs.Add(castArg);

                            string? fmt = method.ParameterFormatters.Count > i ? method.ParameterFormatters[i] : null;
                            if (fmt != null)
                                formattingArgsList.Add($"new {fmt}().Format({castArg})");
                            else
                                formattingArgsList.Add(castArg);
                        }
                        int probeId = probeCounter++;
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(ClassDataSource)");
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(ClassDataSource)");
                        GenerateTestRegistration(sb, method, concreteClassName, concreteMethodName, string.Join(", ", castArgs), string.Join(", ", formattingArgsList), $"{variantSuffix}{classSuffix}({{string.Join(\", \", dataRow)}})", classFixtures, probeId, classRowStr, isClassDataRow, variantName, cv, mv);
                        sb.AppendLine("                }");
                        sb.AppendLine("            }");
                    }
                }

                if (method.MethodDataSources.Count > 0)
                {
                    foreach (var md in method.MethodDataSources)
                    {
                        sb.AppendLine($"            // MethodDataSource: {md.MemberName}");
                        string sourceExpr = md.MemberType != null ? $"TestRunnerExecutor.Services.Get<{md.MemberType}>().{md.MemberName}" : $"TestRunnerExecutor.Services.Get<{concreteClassName}>().{md.MemberName}";
                        if (md.IsMethod) sourceExpr += "(" + string.Join(", ", md.Parameters) + ")";

                        sb.AppendLine($"            foreach (var dataRow in (global::System.Collections.Generic.IEnumerable<object[]>){sourceExpr})");
                        sb.AppendLine("            {");
                        sb.AppendLine("                if (dataRow != null)");
                        sb.AppendLine("                {");
                        
                        var castArgs = new List<string>();
                        var formattingArgsList = new List<string>();

                        for (int i = 0; i < method.ParameterTypes.Count; i++)
                        {
                            string paramType = method.ParameterTypes[i];
                            for (int j = 0; j < method.ClassTypeParams.Count; j++)
                                paramType = paramType.Replace(method.ClassTypeParams[j], cv[j]);
                            for (int j = 0; j < method.MethodTypeParams.Count; j++)
                                paramType = paramType.Replace(method.MethodTypeParams[j], mv[j]);
                            
                            string castArg = $"({paramType})dataRow[{i}]";
                            castArgs.Add(castArg);

                            string? fmt = method.ParameterFormatters.Count > i ? method.ParameterFormatters[i] : null;
                            if (fmt != null)
                                formattingArgsList.Add($"new {fmt}().Format({castArg})");
                            else
                                formattingArgsList.Add(castArg);
                        }
                        int probeId = probeCounter++;
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(MethodDataSource:{md.MemberName})");
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(MethodDataSource:{md.MemberName})");
                        GenerateTestRegistration(sb, method, concreteClassName, concreteMethodName, string.Join(", ", castArgs), string.Join(", ", formattingArgsList), $"{variantSuffix}{classSuffix}({{string.Join(\", \", dataRow)}})", classFixtures, probeId, classRowStr, isClassDataRow, variantName, cv, mv);
                        sb.AppendLine("                }");
                        sb.AppendLine("            }");
                    }
                }

                if (method.DIDataSources.Count > 0)
                {
                    foreach (var md in method.DIDataSources)
                    {
                        sb.AppendLine($"            // DIDataSource: {md.MemberType}");
                        string sourceExpr = string.IsNullOrEmpty(md.MemberName) ? $"TestRunnerExecutor.Services.Get<{md.MemberType}>()" : $"TestRunnerExecutor.Services.Get<{md.MemberType}>().{md.MemberName}";
                        if (md.IsMethod) sourceExpr += "()";

                        sb.AppendLine($"            foreach (var dataRow in (global::System.Collections.Generic.IEnumerable<object[]>){sourceExpr})");
                        sb.AppendLine("            {");
                        sb.AppendLine("                if (dataRow != null)");
                        sb.AppendLine("                {");
                        
                        var castArgs = new List<string>();
                        var formattingArgsList = new List<string>();

                        for (int i = 0; i < method.ParameterTypes.Count; i++)
                        {
                            string paramType = method.ParameterTypes[i];
                            for (int j = 0; j < method.ClassTypeParams.Count; j++)
                                paramType = paramType.Replace(method.ClassTypeParams[j], cv[j]);
                            for (int j = 0; j < method.MethodTypeParams.Count; j++)
                                paramType = paramType.Replace(method.MethodTypeParams[j], mv[j]);
                            
                            string castArg = $"({paramType})dataRow[{i}]";
                            castArgs.Add(castArg);

                            string? fmt = method.ParameterFormatters.Count > i ? method.ParameterFormatters[i] : null;
                            if (fmt != null)
                                formattingArgsList.Add($"new {fmt}().Format({castArg})");
                            else
                                formattingArgsList.Add(castArg);
                        }
                        int probeId = probeCounter++;
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(DIDataSource:{md.MemberType})");
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(DIDataSource:{md.MemberType})");
                        GenerateTestRegistration(sb, method, concreteClassName, concreteMethodName, string.Join(", ", castArgs), string.Join(", ", formattingArgsList), $"{variantSuffix}{classSuffix}({{string.Join(\", \", dataRow)}})", classFixtures, probeId, classRowStr, isClassDataRow, variantName, cv, mv);
                        sb.AppendLine("                }");
                        sb.AppendLine("            }");
                    }
                }

                if (method.CustomDataGenerators.Count > 0)
                {
                    foreach (var gen in method.CustomDataGenerators)
                    {
                        sb.AppendLine($"            // CustomDataGenerator: {gen.AttributeType}");
                        string initArgs = string.Join(", ", gen.Arguments);
                        sb.AppendLine($"            foreach (var dataRow in ((DataSourceGeneratorAttribute)new {gen.AttributeType}({initArgs})).GetData())");
                        sb.AppendLine("            {");
                        sb.AppendLine("                if (dataRow != null)");
                        sb.AppendLine("                {");
                        
                        var castArgs = new List<string>();
                        var formattingArgsList = new List<string>();

                        for (int i = 0; i < method.ParameterTypes.Count; i++)
                        {
                            string paramType = method.ParameterTypes[i];
                            for (int j = 0; j < method.ClassTypeParams.Count; j++)
                                paramType = paramType.Replace(method.ClassTypeParams[j], cv[j]);
                            for (int j = 0; j < method.MethodTypeParams.Count; j++)
                                paramType = paramType.Replace(method.MethodTypeParams[j], mv[j]);
                            
                            string castArg = $"({paramType})dataRow[{i}]";
                            castArgs.Add(castArg);

                            string? fmt = method.ParameterFormatters.Count > i ? method.ParameterFormatters[i] : null;
                            if (fmt != null)
                                formattingArgsList.Add($"new {fmt}().Format({castArg})");
                            else
                                formattingArgsList.Add(castArg);
                        }
                        int probeId = probeCounter++;
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(CustomDataGenerator:{gen.AttributeType})");
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(CustomDataGenerator:{gen.AttributeType})");
                        GenerateTestRegistration(sb, method, concreteClassName, concreteMethodName, string.Join(", ", castArgs), string.Join(", ", formattingArgsList), $"{variantSuffix}{classSuffix}({{string.Join(\", \", dataRow)}})", classFixtures, probeId, classRowStr, isClassDataRow, variantName, cv, mv);
                        sb.AppendLine("                }");
                        sb.AppendLine("            }");
                    }
                }
            }
            else
            {
                int probeId = probeCounter++;
                coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}");
                GenerateTestRegistration(sb, method, concreteClassName, concreteMethodName, "", "", $"{variantSuffix}{classSuffix}", classFixtures, probeId, classRowStr, isClassDataRow, variantName, cv, mv);
            }
        }

        private static void GenerateTestRegistration(StringBuilder sb, TestMethodModel method, string concreteClassName, string concreteMethodName, string args, string? formattingArgs, string displayNameSuffix, List<string> classFixtures, int probeId, string? classRowStr, bool isClassDataRow, string? variantName, string[] cv, string[] mv)
        {
            sb.AppendLine("            list.Add(new ProvaTest");
            sb.AppendLine("            {");

            string displayNameExpr;
            if (method.DisplayNameFormat != null)
            {
                 var fArgs = formattingArgs ?? args;
                 // If args are empty, just use the format string. Otherwise format it.
                 if (string.IsNullOrWhiteSpace(fArgs))
                     displayNameExpr = $"\"{method.DisplayNameFormat}\"";
                 else
                     displayNameExpr = $"string.Format(\"{method.DisplayNameFormat}\", {fArgs})";
            }
            else
            {
                displayNameExpr = "$\"" + concreteClassName + "." + concreteMethodName + displayNameSuffix + "\"";
            }
            
            sb.AppendLine($"                DisplayName = {displayNameExpr},");
            sb.AppendLine($"                FullName = $\"{concreteClassName}.{concreteMethodName}\",");
            sb.AppendLine($"                ClassName = \"{concreteClassName}\",");
            sb.AppendLine($"                CoverageId = {probeId},");
            sb.AppendLine($"                Description = {(method.Description != null ? $"\"{method.Description.Replace("\"", "\\\"")}\"" : "null")},");
            sb.AppendLine($"                SkipReason = {(method.SkipReason != null ? $"\"{method.SkipReason.Replace("\"", "\\\"")}\"" : "null")},");
            sb.AppendLine($"                RetryCount = {(method.RetryCount.HasValue ? method.RetryCount.Value.ToString(global::System.Globalization.CultureInfo.InvariantCulture) : "null")},");
            sb.AppendLine($"                MaxParallel = {(method.MaxParallel.HasValue ? method.MaxParallel.Value.ToString(global::System.Globalization.CultureInfo.InvariantCulture) : "null")},");
            sb.AppendLine($"                DoNotParallelize = {(method.DoNotParallelize ? "true" : "false")},");
            sb.AppendLine($"                Repeat = {(method.RepeatCount.HasValue ? method.RepeatCount.Value.ToString(global::System.Globalization.CultureInfo.InvariantCulture) : "null")},");
            sb.AppendLine($"                Timeout = {(method.TimeoutMs.HasValue ? method.TimeoutMs.Value.ToString(global::System.Globalization.CultureInfo.InvariantCulture) : "null")},");
            sb.AppendLine($"                Culture = {(method.Culture != null ? $"\"{method.Culture}\"" : "null")},");
            sb.AppendLine($"                ExecutorType = {(method.TestExecutorType != null ? $"typeof({method.TestExecutorType})" : "null")},");
            sb.AppendLine($"                Variant = {(variantName != null ? $"\"{variantName}\"" : "null")},");

            if (method.ClassBefore.Count > 0)
                sb.AppendLine($"                ClassBefore = new Func<Task>[] {{ {string.Join(", ", method.ClassBefore.Select(h => h.IsAsync ? $"async () => await {concreteClassName}.{h.Name}()" : $"() => {{ {concreteClassName}.{h.Name}(); return Task.CompletedTask; }}"))} }},");
            if (method.ClassAfter.Count > 0)
                sb.AppendLine($"                ClassAfter = new Func<Task>[] {{ {string.Join(", ", method.ClassAfter.Select(h => h.IsAsync ? $"async () => await {concreteClassName}.{h.Name}()" : $"() => {{ {concreteClassName}.{h.Name}(); return Task.CompletedTask; }}"))} }},");
            
            if (classFixtures.Count > 0)
                sb.AppendLine($"                ClassFixtures = new Func<Task>[] {{ {string.Join(", ", classFixtures.Select(f => $"async () => {{ var f = TestRunnerExecutor.Services.Get<{f}>(); if ((object)f is IAsyncLifetime al) await al.InitializeAsync(); }}"))} }},");

            if (method.ResourceConstraints != null && method.ResourceConstraints.Count > 0)
                sb.AppendLine($"                ResourceConstraints = new string[] {{ {string.Join(", ", method.ResourceConstraints.Select(r => $"\"{r}\""))} }},");

            if (method.ParallelLimiters != null && method.ParallelLimiters.Count > 0)
                sb.AppendLine($"                ParallelLimiters = new (string, int)[] {{ {string.Join(", ", method.ParallelLimiters.Select(l => $"(\"{l.Key}\", {l.Limit})"))} }},");

            if (!string.IsNullOrEmpty(method.ParallelGroup))
                sb.AppendLine($"                ParallelGroup = \"{method.ParallelGroup}\",");

            if (method.Properties.Count > 0)
                sb.AppendLine("                Properties = new Dictionary<string, string> { " + string.Join(", ", method.Properties.Select(p => $"{{ \"{p.Key}\", \"{p.Value}\" }}")) + " },");
            sb.AppendLine("                ExecuteDelegate = async () => {");
            if (!method.IsStatic)
            {
                sb.AppendLine($"                    {concreteClassName}? instance = null;");
            }
            sb.AppendLine($"                    CoverageRegistry.Hit({probeId});");
            sb.AppendLine("                    var originalCulture = global::System.Globalization.CultureInfo.CurrentCulture;");
            sb.AppendLine("                    var originalUICulture = global::System.Globalization.CultureInfo.CurrentUICulture;");
            sb.AppendLine();
            if (method.UsesOutputHelper)
            {
                 sb.AppendLine("                    var outputHelper = new Prova.TestOutputHelper();");
            }

            sb.AppendLine("                    try {");
            if (!string.IsNullOrEmpty(method.Culture))
            {
                sb.AppendLine($"                        var culture = new global::System.Globalization.CultureInfo(\"{method.Culture}\");");
                sb.AppendLine("                        global::System.Globalization.CultureInfo.CurrentCulture = culture;");
                sb.AppendLine("                        global::System.Globalization.CultureInfo.CurrentUICulture = culture;");
            }

            if (method.IsStatic)
            {
                 sb.AppendLine("                    try {");
                  {
                      if (method.ReturnsVoid)
                          sb.AppendLine($"                        {concreteClassName}.{concreteMethodName}({args});");
                      else
                          sb.AppendLine($"                        await {concreteClassName}.{concreteMethodName}({args});");
                  }
                 sb.AppendLine("                    } finally { }");
            }
            else
            {
                foreach (var fixtureType in classFixtures)
                {
                    var varName = $"fixture_{fixtureType.Replace(".", "_")}";
                    sb.AppendLine($"                    var {varName} = TestRunnerExecutor.Services.Get<{fixtureType}>();");
                }
                
                var constructorArgs = new List<string>();
                int classDataIdx = 0;
                for (int i = 0; i < method.Dependencies.Count; i++)
                {
                    var dep = method.Dependencies[i];
                    var paramType = method.ConstructorParameterTypes.Count > i ? method.ConstructorParameterTypes[i] : "object";

                    if (dep.StartsWith("__CLASS_DATA_", StringComparison.Ordinal))
                    {
                        if (classRowStr != null)
                        {
                             if (isClassDataRow)
                                 constructorArgs.Add($"({paramType}){classRowStr}[{classDataIdx}]"); 
                             else
                                 constructorArgs.Add(classRowStr.Split(',')[classDataIdx].Trim());
                        }
                        else
                        {
                             constructorArgs.Add("default"); 
                        }
                        classDataIdx++;
                    }
                    else if (dep == "outputHelper")
                    {
                        constructorArgs.Add("outputHelper");
                    }
                    else
                    {
                        constructorArgs.Add(dep);
                    }
                }

                if (method.ClassFactoryType != null)
                {
                    sb.AppendLine($"                    var factory = TestRunnerExecutor.Services.Get<{method.ClassFactoryType}>();");
                    sb.AppendLine($"                    instance = factory.CreateInstance(TestRunnerExecutor.Services);");
                }
                else
                {
                    sb.AppendLine($"                    instance = new {concreteClassName}({string.Join(", ", constructorArgs)});");
                }
                sb.AppendLine("                    try {");
                
                if (method.LifecycleBefore.Count > 0)
                {
                    sb.AppendLine("                        // Lifecycle: Before");
                    foreach (var hook in method.LifecycleBefore) 
                    {
                        string executorType = hook.ExecutorType != null ? $"typeof({hook.ExecutorType})" : "null";
                        string hookAction = hook.IsAsync ? $"async () => await instance.{hook.Name}()" : $"() => {{ instance.{hook.Name}(); return global::System.Threading.Tasks.Task.CompletedTask; }}";
                        sb.AppendLine($"                        await TestRunnerExecutor.InvokeHookHelper({hookAction}, \"{hook.Name}\", {executorType});");
                    }
                }
                
                if (method.ImplementsAsyncLifetime) sb.AppendLine("                        if ((object)instance is IAsyncLifetime asyncLife) await asyncLife.InitializeAsync();");

                if (method.MaxAllocBytes.HasValue)
                {
                    sb.AppendLine($"                        long maxAlloc = {method.MaxAllocBytes.Value};");
                    sb.AppendLine("                        long startAlloc = global::System.GC.GetAllocatedBytesForCurrentThread();");
                }

                  {
                      if (method.IsFsCheckProperty)
                      {
                        var resolvedTypes = new global::System.Collections.Generic.List<string>();
                        for (int i = 0; i < method.ParameterTypes.Count; i++)
                        {
                            string paramType = method.ParameterTypes[i];
                            for (int j = 0; j < method.ClassTypeParams.Count; j++) paramType = paramType.Replace(method.ClassTypeParams[j], cv[j]);
                            for (int j = 0; j < method.MethodTypeParams.Count; j++) paramType = paramType.Replace(method.MethodTypeParams[j], mv[j]);
                            resolvedTypes.Add(paramType);
                        }
                        var typeArgs = string.Join(", ", resolvedTypes);
                        var pVars = global::System.Linq.Enumerable.Range(0, resolvedTypes.Count).Select(k => $"p{k}").ToList();
                        var pDecl = "(" + string.Join(", ", pVars) + ")";
                        var pCall = string.Join(", ", pVars);
                        
                        sb.AppendLine($"                    var fsCheckProp = global::FsCheck.Prop.ForAll<{typeArgs}>({pDecl} => {{");
                         if (method.ReturnsVoid)
                              sb.AppendLine($"                        instance.{concreteMethodName}({pCall});");
                          else
                              sb.AppendLine($"                        instance.{concreteMethodName}({pCall}).GetAwaiter().GetResult();");
                        sb.AppendLine("                    });");
                        
                        sb.AppendLine("                    var fsConfig = new global::System.Collections.Generic.Dictionary<string, string>();");
                         if (method.FsCheckConfig != null)
                             foreach(var kvp in method.FsCheckConfig) sb.AppendLine($"                    fsConfig[\"{kvp.Key}\"] = \"{kvp.Value}\";");

                        sb.AppendLine("                    Prova.FsCheck.FsCheckRunner.Run(fsConfig, fsCheckProp);");
                      }
                      else
                      {
                          if (method.ReturnsVoid)
                              sb.AppendLine($"                        instance.{concreteMethodName}({args});");
                          else
                              sb.AppendLine($"                        await instance.{concreteMethodName}({args});");
                      }
                  }

                if (method.MaxAllocBytes.HasValue)
                {
                    sb.AppendLine("                        long endAlloc = global::System.GC.GetAllocatedBytesForCurrentThread();");
                    sb.AppendLine("                        if (endAlloc - startAlloc > maxAlloc) throw new global::System.Exception($\"Memory allocation exceeded: {endAlloc - startAlloc} > {maxAlloc}\");");
                }

                if (method.MockFields.Count > 0)
                {
                    foreach (var mockName in method.MockFields) sb.AppendLine($"                        instance.{mockName}.VerifyAll();");
                }

                sb.AppendLine("                    } finally {");

                if (method.LifecycleAfter.Count > 0)
                {
                    sb.AppendLine("                        // Lifecycle: After");
                    foreach (var hook in method.LifecycleAfter)
                    {
                        string executorType = hook.ExecutorType != null ? $"typeof({hook.ExecutorType})" : "null";
                        string hookAction = hook.IsAsync ? $"async () => await instance.{hook.Name}()" : $"() => {{ instance.{hook.Name}(); return global::System.Threading.Tasks.Task.CompletedTask; }}";
                        sb.AppendLine($"                        try {{ await TestRunnerExecutor.InvokeHookHelper({hookAction}, \"{hook.Name}\", {executorType}); }} catch {{ }}");
                    }
                }
                if (method.ImplementsAsyncLifetime) sb.AppendLine("                        if ((object)instance is IAsyncLifetime asyncLifeDisp) await asyncLifeDisp.DisposeAsync();");
                sb.AppendLine("                        if ((object)instance is global::System.IDisposable d) d.Dispose();");
                sb.AppendLine("                    }");
            }
                sb.AppendLine("                    } finally {");
                sb.AppendLine("                        global::System.Globalization.CultureInfo.CurrentCulture = originalCulture;");
                sb.AppendLine("                        global::System.Globalization.CultureInfo.CurrentUICulture = originalUICulture;");
                sb.AppendLine("                    }");
                sb.AppendLine($"                    return {(method.UsesOutputHelper ? "outputHelper.Output" : "null")};");
                sb.AppendLine("                }");
            sb.AppendLine("            });");
        }
        private static string RemoveGenerics(string name)
        {
            int idx = name.LastIndexOf('<');
            return idx != -1 ? name.Substring(0, idx) : name;
        }
