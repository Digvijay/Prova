        private static void EmitMethodExecution(StringBuilder sb, TestMethodModel method, string concreteClassName, string concreteMethodName, string variantSuffix, string? classRowStr, string classSuffix, List<string> classFixtures, ref int probeCounter, List<string> coverageMetadata, string[] cv, string[] mv, string? variantName, bool isClassDataRow = false)
        {
            if (method.CombinatorialValues != null && method.CombinatorialValues.Count > 0)
            {
                // Combinatorial Execution (Matrix)
                var paramVars = new List<string>();
                var indent = "            ";
                
                // Nest loops
                for (int i = 0; i < method.CombinatorialValues.Count; i++)
                {
                    var (paramName, values) = method.CombinatorialValues[i];
                    var valArray = "new [] { " + string.Join(", ", values) + " }";
                    var loopVar = $"p{i}_{paramName}";
                    paramVars.Add(loopVar);

                    sb.AppendLine($"{indent}foreach (var {loopVar} in {valArray})");
                    sb.AppendLine($"{indent}{{");
                    indent += "    ";
                }

                // Inner Body
                int probeId = probeCounter++;
                var args = string.Join(", ", paramVars);

                var formattingArgsList = new List<string>();
                for (int i = 0; i < method.CombinatorialValues.Count; i++)
                {
                    string? fmt = method.ParameterFormatters.Count > i ? method.ParameterFormatters[i] : null;
                    if (fmt != null)
                        formattingArgsList.Add($"new {fmt}().Format({paramVars[i]})");
                    else
                        formattingArgsList.Add(paramVars[i]);
                }
                var formattingArgs = string.Join(", ", formattingArgsList);

                var displayArgs = string.Join(", ", paramVars.Select(v => "{" + v + "}")); // Interpolated string for display
                
                coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(Matrix)");
                coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(Matrix)");
                GenerateTestRegistration(sb, method, concreteClassName, concreteMethodName, args, formattingArgs, $"{variantSuffix}{classSuffix}({displayArgs})", classFixtures, probeId, classRowStr, isClassDataRow, variantName, cv, mv);

                // Close loops
                for (int i = 0; i < method.CombinatorialValues.Count; i++)
                {
                    indent = indent.Substring(0, indent.Length - 4);
                    sb.AppendLine($"{indent}}}");
                }
            }
            else if (method.IsTheory)
            {
                if (method.TestData.Count > 0)
                {
                    foreach (var dataRow in method.TestData)
                    {
                        int probeId = probeCounter++;
                        string rowString = string.Join(", ", dataRow);
                        // For InlineData, we rely on the rowString being pre-formatted or basic types. 
                        // But if we want formatters to work here, we'd need to cast and format.
                        // Currently InlineData values are injected as literals. 
                        // To support formatters, we would need to wrap the literals? 
                        // Complex: The literal "1" is int. new HexFormatter().Format(1).
                        // Simpler to treat InlineData without formatters for now, OR:
                        // We generated rowString as literals. We can try to wrap them.
                        // But we don't know the types easily here without parsing the literals.
                        // Let's pass 'rowString' as formattingArgs for now (no-op for InlineData unless we parse).
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(Row_{probeId})");
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(Row_{probeId})");
                        GenerateTestRegistration(sb, method, concreteClassName, concreteMethodName, rowString, rowString, $"{variantSuffix}{classSuffix}(Row_{probeId})", classFixtures, probeId, classRowStr, isClassDataRow, variantName, cv, mv);
                    }
                }

                if (method.MemberData.Count > 0)
                {
                    foreach (var md in method.MemberData)
                    {
                        sb.AppendLine($"            // MemberData: {md.MemberName}");
                        string sourceExpr = md.MemberType != null ? md.MemberType + "." + md.MemberName : concreteClassName + "." + md.MemberName;
                        if (md.IsMethod) sourceExpr += "(" + string.Join(", ", md.Parameters) + ")";

                        sb.AppendLine($"            foreach (var dataRow in (global::System.Collections.Generic.IEnumerable<object[]>){sourceExpr})");
                        sb.AppendLine("            {");
                        sb.AppendLine("                if (dataRow != null)");
                        sb.AppendLine("                {");
                        
                        var castArgs = new List<string>();
                        var formattingArgsList = new List<string>();

                        for (int i = 0; i < method.ParameterTypes.Count; i++)
                        {
                            string paramType = method.ParameterTypes[i];
                            for (int j = 0; j < method.ClassTypeParams.Count; j++)
                                paramType = paramType.Replace(method.ClassTypeParams[j], cv[j]);
                            for (int j = 0; j < method.MethodTypeParams.Count; j++)
                                paramType = paramType.Replace(method.MethodTypeParams[j], mv[j]);
                            
                            string castArg = $"({paramType})dataRow[{i}]";
                            castArgs.Add(castArg);

                            string? fmt = method.ParameterFormatters.Count > i ? method.ParameterFormatters[i] : null;
                            if (fmt != null)
                                formattingArgsList.Add($"new {fmt}().Format({castArg})");
                            else
                                formattingArgsList.Add(castArg);
                        }
                        int probeId = probeCounter++;
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(MemberData:{md.MemberName})");
                        coverageMetadata.Add($"{concreteClassName}.{concreteMethodName}{variantSuffix}{classSuffix}(MemberData:{md.MemberName})");
                        GenerateTestRegistration(sb, method, concreteClassName, concreteMethodName, string.Join(", ", castArgs), string.Join(", ", formattingArgsList), $"{variantSuffix}{classSuffix}({{string.Join(\", \", dataRow)}})", classFixtures, probeId, classRowStr, isClassDataRow, variantName, cv, mv);
                        sb.AppendLine("                }");
                        sb.AppendLine("            }");
                    }
                }

                if (method.ClassData.Count > 0)
                {
                    foreach (var cdType in method.ClassData)
                    {
                        sb.AppendLine($"            // ClassData: {cdType}");
                        sb.AppendLine($"            foreach (var dataRow in (global::System.Collections.Generic.IEnumerable<object[]>)new {cdType}())");
                        sb.AppendLine("            {");
                        sb.AppendLine("                if (dataRow != null)");
                        sb.AppendLine("                {");
                        
